#include <stdio.h>

int main() {
    int num = 1000000000;
    long long int num1 = 1000000000000;

    printf("Integer value: %d\n", num);
    printf("Long integer value: %lld\n", num1);


    float x = 23.888888;
    double y = 2838333.93423889;

    printf("Floating point number: %f\n", x);
    printf("Double precision floating point number:%lf\n", y);
}